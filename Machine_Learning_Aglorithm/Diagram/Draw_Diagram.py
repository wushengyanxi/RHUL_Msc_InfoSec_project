import matplotlib.pyplot as plt
import os

def plot_four_lines(list1, list2, list3, list4, save_path):
    list_length = len(list1)
    x_values = list(range(1, list_length + 1))

    plt.figure(figsize=(10, 6))

    plt.plot(x_values, list1, label='Precision_LR',marker='o', markersize=2)
    plt.plot(x_values, list2, label='Precision_KNN',marker='o', markersize=2)
    plt.plot(x_values, list3, label='Precision_SVM',marker='o', markersize=2)
    plt.plot(x_values, list4, label='Precision_SoftMax',marker='o', markersize=2)

    plt.xticks(range(1, list_length + 1, 1))
    
    plt.ylim(0.75, 0.95)

    plt.legend()

    plt.savefig(save_path)

    plt.close()

